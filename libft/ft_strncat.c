#include "libft.h"

char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int				idest;
	unsigned int	isrc;

	idest = 0;
	isrc = 0;
	while (dest[idest] != '\0')
	{
		idest++;
	}
	while (src[isrc] != '\0' && isrc != nb)
	{
		dest[idest] = src[isrc];
		idest++;
		isrc++;
	}
	dest[idest] = '\0';
	return (dest);
}
